/**
 * scripts/build-bookmarklet.js
 * Compiles src/utils/dfmBookmarklet.js into a single-line bookmarklet string.
 * Outputs: src/utils/bookmarkletCode.js (importable ES module)
 * Usage: npm run build:bookmarklet
 */
import { minify } from 'terser';
import { readFileSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const srcPath = resolve(__dirname, '../src/utils/dfmBookmarklet.js');
const outPath = resolve(__dirname, '../src/utils/bookmarkletCode.js');

let src = readFileSync(srcPath, 'utf-8');

// Strip leading "javascript:" if present, since terser doesn't understand it
if (src.trimStart().startsWith('javascript:')) {
  src = src.trimStart().replace(/^javascript:\s*/, '');
}

const result = await minify(src, {
  compress: {
    drop_console: false, // keep console.log for debugging
    passes: 2,
  },
  mangle: true,
  format: {
    comments: false, // strip all comments
  },
});

if (result.error) {
  console.error('❌ Minification failed:', result.error);
  process.exit(1);
}

// Output as an ES module so it can be imported in the Vite/React app
const moduleContent = `// ⚠️ Auto-generated by scripts/build-bookmarklet.js — Do not edit manually!
// Source: src/utils/dfmBookmarklet.js
// Run \`npm run build:bookmarklet\` to regenerate.
export const bookmarkletCode = ${JSON.stringify(result.code)};
`;
writeFileSync(outPath, moduleContent, 'utf-8');

console.log(`✅ Bookmarklet module written: ${outPath}`);
console.log(`📏 Size: ${result.code.length} chars (original: ${src.length} chars)`);
